package day04_variables_intro;

public class FirstVariable {
    public static void main(String[] args){
//        declare variable n that can store int(whole numbers)
        int n;
//        assign value 10 to n
        n=5458;
        System.out.println(n);
        System.out.println("n");
        int apples;
        apples=2839;
        System.out.println(apples);
        System.out.println(n+apples);
        int bananas;
        bananas=6573;
        System.out.println(n+apples*bananas);
    }
}

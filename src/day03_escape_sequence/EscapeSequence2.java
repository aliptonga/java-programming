package day03_escape_sequence;

public class EscapeSequence2 {
    public static void main(String[] args){
        System.out.println("\t\"iphone case\"\n \t\"iphone\"\n \t\"xbox\"\n \t\"webcam\"\n \t\"monitor\"");
        System.out.println("\t\t  ^");
        System.out.println("\t/\t\t\t\\");
        System.out.println("\t|\t\t\t|");
        System.out.println("\t____________");
    }
}
